import SwiftUI

extension Font {
    static func montserratBlack(ofSize: CGFloat) -> Font {
        return Font.custom("Montserrat-Black", size: ofSize)
    }
    static func montserratBold(ofSize: CGFloat) -> Font {
        return Font.custom("Montserrat-Bold", size: ofSize)
    }
    static func montserratExtraBold(ofSize: CGFloat) -> Font {
        return Font.custom("Montserrat-ExtraBold", size: ofSize)
    }
    static func montserratExtraLight(ofSize: CGFloat) -> Font {
        return Font.custom("Montserrat-ExtraLight", size: ofSize)
    }
    static func montserratLight(ofSize: CGFloat) -> Font {
        return Font.custom("Montserrat-Light", size: ofSize)
    }
    static func montserratMedium(ofSize: CGFloat) -> Font {
        return Font.custom("Montserrat-Medium", size: ofSize)
    }
    static func montserratRegular(ofSize: CGFloat) -> Font {
        return Font.custom("Montserrat-Regular", size: ofSize)
    }
    static func montserratSemiBold(ofSize: CGFloat) -> Font {
        return Font.custom("Montserrat-SemiBold", size: ofSize)
    }
    static func montserratThin(ofSize: CGFloat) -> Font {
        return Font.custom("Montserrat-Thin", size: ofSize)
    }
}
